    private void setEmpId() {
        try{
            String id = EmpDao.getNextEmpId();
            jtxtEmpId.setText(id);
            jtxtSal.setText("");
            jtxtEmpName.setText("");
            jcbJob.setSelectedIndex(0);
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error In DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        
        
    }

    private boolean validInput() {
        String empname=jtxtEmpName.getText().trim();
        String empsal=jtxtSal.getText().trim();
        if(!empname.isEmpty() && !empsal.isEmpty())
            return true;
        return false;
    }

    

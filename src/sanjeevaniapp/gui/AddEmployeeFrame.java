    private void btnAddEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddEmpActionPerformed
        try{
            if(!validInput())
            {
                JOptionPane.showMessageDialog(null,"Please input Data!","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
            EmpPojo emp = new EmpPojo();
            emp.setEmpId(jtxtEmpId.getText().trim());
            emp.setEmpName(jtxtEmpName.getText().trim());
            emp.setEmpSal(Double.parseDouble(jtxtSal.getText().trim()));
            emp.setEmpDept(jcbJob.getSelectedItem().toString().trim());
            
            boolean result = EmpDao.addEmp(emp);
            if(result)
            {
                JOptionPane.showMessageDialog(null,"Record Added!","Sucsess",JOptionPane.INFORMATION_MESSAGE);
                setEmpId();
            }
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error In DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"Please input numeric salary!"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnAddEmpActionPerformed


private void loadPatId() {
        try{
            List<String> ls = PatientDao.getAllPatientsId();
            for(String pat:ls){
                jcbPatientId.addItem(pat);
            }
        }
            catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error in DB:"+ex);
            System.out.println("Error in DB");
            ex.printStackTrace();
        }
    }

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String patid=jcbPatientId.getSelectedItem().toString();
        int ans;
        ans=JOptionPane.showConfirmDialog(null,"Are You Sure?","Deleting!",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(ans==JOptionPane.YES_OPTION)
        {
        try{
            boolean result=PatientDao.deletePatient(patid);
            if(result){
                JOptionPane.showMessageDialog(null, "Patient removed successfully!","Successful",JOptionPane.INFORMATION_MESSAGE);
                jcbPatientId.removeItem(patid);
                if(jcbPatientId.getItemCount()==0){
                    btnDelete.setEnabled(false);
                    JOptionPane.showMessageDialog(null, "All Patient removed!","Warnning",JOptionPane.WARNING_MESSAGE);
                
            }
        }else{
              JOptionPane.showMessageDialog(null, "Deletion unsuccessful");  
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error in DB:"+ex);
            ex.printStackTrace();
        }}
    }//GEN-LAST:event_btnDeleteActionPerformed

